"use strict";

RecipeDB.changepicture = {};
RecipeDB.changepicture.transient = {};
RecipeDB.changepicture.constants = {};

RecipeDB.changepicture.constants.UPLOAD_BUTTON_ID = "Upload-Picture-Button";
RecipeDB.changepicture.constants.NOTIFICATION_BOX_ID = "Dialog-Notification-Box";
RecipeDB.changepicture.constants.SELECT_PICTURE_BUTTON_ID = "Select-Picture-Button";
RecipeDB.changepicture.constants.IMAGE_PREVIEW_ID = "Preview-Image";
RecipeDB.changepicture.constants.NEW_PICTURE_ID = "New-Picture";
RecipeDB.changepicture.constants.PICTURE_UPLOAD_FORM_ID = "Change-Picture-Form";
RecipeDB.changepicture.constants.MAX_FILE_SIZE = 0; // Values will be generated by CF upon init
RecipeDB.changepicture.constants.ACCEPTED_MIME_TYPES = ""; // Values will be generated by CF upon init

RecipeDB.changepicture.init = function() {

	// If the browser doesn't support client side binary handling we make sure file uploads are disabled
	if (RecipeDB.main.AJAXFileSubmitSupported() === false) {

		RecipeDB.main.notifyUserOfError(
			$("#" + RecipeDB.changepicture.constants.NOTIFICATION_BOX_ID),
			"Your browser is decrepit and cannot handle the modern ways of technology. No file uploading for you!"
		);
		$("#" + RecipeDB.changepicture.constants.NEW_PICTURE_ID).prop("disabled", true);

	};

	RecipeDB.changepicture.constants.MAX_FILE_SIZE = parseInt(RecipeDB.changepicture.constants.MAX_FILE_SIZE); //Thanks Lucee for casting my numeric value to a string...
	$("#" + RecipeDB.changepicture.constants.NEW_PICTURE_ID).attr("accept", RecipeDB.changepicture.constants.ACCEPTED_MIME_TYPES);

	/* Event handlers */
	$("#" + RecipeDB.changepicture.constants.NEW_PICTURE_ID).change(RecipeDB.changepicture.onChangeFile);
	$("#" + RecipeDB.changepicture.constants.UPLOAD_BUTTON_ID).click(RecipeDB.changepicture.uploadPicture);

	console.log("Change picture init complete");
};

RecipeDB.changepicture.uploadPicture = function() {

	var RecipeID = parseInt($("#" + RecipeDB.page.constants.RECIPEID_ID).val());
	var FormToPost = new FormData(
		document.getElementById(RecipeDB.changepicture.constants.PICTURE_UPLOAD_FORM_ID)
	);

	FormToPost.append("recipeID", RecipeID);
	FormToPost.append("authKey", RecipeDB.main.constants.AUTH_KEY);

	if (RecipeDB.main.transient.ajaxCallInProgress === false) {
		RecipeDB.main.transient.ajaxCallInProgress = true
	} else {
		return false;
	};

	$.ajax({
		type: "POST",
		timeout: RecipeDB.main.constants.AJAX_TIMEOUT,
		url: "Controllers/UploadRecipePicture.cfm",
		dataType: "json",
		enctype: "multipart/form-data",
		processData: false, /* These two are required for file posting via AJAX to work */
		contentType: false, /* These two are required for file posting via AJAX to work */
		data: FormToPost,

		complete: function() {
			RecipeDB.main.transient.ajaxCallInProgress = false;
			RecipeDB.main.ajaxLoadButton(false, $('#' + RecipeDB.changepicture.constants.UPLOAD_BUTTON_ID));
		},
		error: function() {
			$("#" + RecipeDB.main.constants.DIALOG_ID).dialog("close");
			RecipeDB.main.onAJAXCallError(arguments);
		},
		beforeSend: function() {
			RecipeDB.main.ajaxLoadButton(true, $('#' + RecipeDB.changepicture.constants.UPLOAD_BUTTON_ID));
		},
		success: function() {
			RecipeDB.changepicture.onPictureUploaded(arguments[0]);
		}

	});

};

RecipeDB.changepicture.onChangeFile = function() {

	var PictureFileInput,FileSize,FileType,FormattedSize,Message,FileAccepted;

	$("#" + RecipeDB.changepicture.constants.NOTIFICATION_BOX_ID).hide();
	PictureFileInput = document.getElementById(RecipeDB.changepicture.constants.NEW_PICTURE_ID);

	if (PictureFileInput.files.length === 0) {
		return
	};

	FileSize = PictureFileInput.files[0].size;
	FileType = PictureFileInput.files[0].type;
	FormattedSize = RecipeDB.changepicture.getFormattedSize(FileSize);

	if (FileSize > RecipeDB.changepicture.constants.MAX_FILE_SIZE) {
		Message = "The file you selected is too big: " + FormattedSize
		FileAccepted = false;
	} else {
		FileAccepted = true;
	};

	if ( RecipeDB.changepicture.constants.ACCEPTED_MIME_TYPES.indexOf(FileType) === -1 || FileType.length === 0 ) {

		if (Message.length > 0) {
			Message += "<br/>"
		}
		if (FileType.length === 0) {
			Message += "Filetype is unknown, which usually means it's not accepted"
		} else {
			Message += "The file you selected is not an accepted type: " + FileType
		}
		FileAccepted = false

	} else {
		FileAccepted = true;
	};

	if (FileAccepted === false) {

		RecipeDB.main.notifyUserOfError( $("#" + RecipeDB.changepicture.constants.NOTIFICATION_BOX_ID), Message);
		RecipeDB.changepicture.disableUpload(true);

	} 
	else if (FileAccepted === true) {
		RecipeDB.changepicture.disableUpload(false)
	}
	else {
		RecipeDB.main.onJavascriptError("FileAccepted variable is unknown, a logic error must have happened", "RecipeDB.changepicture.onChangeFile")
	}
};

RecipeDB.changepicture.getFormattedSize = function(SizeInBytes) {
	SizeInBytes = parseInt(SizeInBytes);

	if (SizeInBytes < 1024) {
		return SizeInBytes.toFixed(2) + " bytes";
	}
	if (SizeInBytes < Math.pow(1024,2)) {
		return (SizeInBytes/1024).toFixed(2) + " kilobytes";
	}
	if (SizeInBytes < Math.pow(1024,3)) {
		return (SizeInBytes/Math.pow(1024,2)).toFixed(2) + " megabytes";
	}

	return (SizeInBytes/Math.pow(1024,3)).toFixed(2) + " gigabytes";
};

RecipeDB.changepicture.disableUpload = function(State) {

	var UploadButton = $("#" + RecipeDB.changepicture.constants.UPLOAD_BUTTON_ID);

	if (State === true) {
		UploadButton.prop("disabled", true);
		UploadButton.removeClass("standard-button");
		UploadButton.addClass("disabled-input");
	}
	else if (State === false) {
		UploadButton.prop("disabled", false);
		UploadButton.removeClass("disabled-input");
		UploadButton.addClass("standard-button");
	}
};

RecipeDB.changepicture.onPictureUploaded = function(ControllerResponse) {
	var MessageBox = $("#" + RecipeDB.page.constants.NOTICATION_ELEMENT_ID);

	if (ControllerResponse.statuscode === 0) {

		$("#" + RecipeDB.main.constants.DIALOG_ID).dialog("close");
		$("#" + RecipeDB.page.constants.PICTURE_ID).attr("src", "Controllers/RecipeImageDownloader.cfm?fileName=" + ControllerResponse.data);
		RecipeDB.main.notifyUserOfSuccess( MessageBox, "PICTURE CHANGED", 2000);

	} else {
		RecipeDB.main.onJavascriptError(ControllerResponse, "RecipeDB.changepicture.onPictureUploaded");
	}
};